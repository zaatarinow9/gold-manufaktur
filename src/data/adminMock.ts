const productById = new Map(managedProducts.map((product) => [product.id, product]));

function getProduct(productId: string) {
  const product = productById.get(productId);

  if (!product) {
    throw new Error(`Unknown admin product: ${productId}`);
  }

  return product;
}

function buildOrderItem(productId: string, quantity: number, selectedOptions: string[]) {
  const product = getProduct(productId);

  return {
    id: `item-${productId}`,
    productId: product.id,
    productName: product.name,
    productImage: product.imageUrl,
    categorySlug: product.categorySlug,
    categoryName: product.categoryName,
    quantity,
    referenceImages: product.gallery,
    selectedOptions: selectedOptions.map((optionId) => {
      const option = globalOptions.find((item) => item.id === optionId);

      return {
        optionId,
        key: option?.key ?? optionId,
        label: option?.label ?? optionId,
        value: option?.values[0]?.value ?? "",
        groupKey: option?.groupKey ?? "workshop_notes",
      };
    }),
  };
}

function buildMockTrackingNumber(sequence: number) {
  return buildTrackingNumber(mockTrackingDate, sequence);
}

